#Escriba un programa que, dada una lista, devuelve una nueva lista cuyo contenido sea igual a la
#original pero invertida. Así, dada la lista ['Di', 'buen', 'día', 'a', 'papa'], 
#deberá devolver ['papa', 'a',
#'día', 'buen', 'Di'].

#1. Ingrese una lista
listado_palabras = ["Di", "buen", "día", "a", "papa"]

#2. Invertir lista
